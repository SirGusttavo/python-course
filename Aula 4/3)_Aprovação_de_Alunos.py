# 3. Aprovação de Alunos:
# Solicite ao usuário a nota do aluno.
# Se a nota for maior ou igual a 6, exiba "Aluno aprovado!". Caso contrário, exiba "Aluno reprovado."

nota = float(input('Digite a nota do aluno: '))
if nota >= 6:
    print ('Aluno aprovado!')
else:
    print('Aluno reprovado!')   