# 5. Maior entre Dois Números: 
# Escreva um programa que peça ao usuário dois números e determine qual é o maior.

numero1 = float(input("Digite um número: "))
numero2 = float(input("Digite outro número: "))
if numero1 > numero2:
    print("O primeiro número digitado é maior que o segundo número digitado")
else:
    print ("O segundo número digitado é maior que o primeiro número digitado")
    