# 4. Aprovação em Prova: 
# Crie um programa que pergunte ao usuário sua nota em uma prova e, com base na nota, informe se ele passou ou não (nota mínima para aprovação: 6).

nota = float(input("Digite sua nota da prova: "))
if nota >= 6:
        print("Aluno aprovado!")
else:
    print("Aluno reprovado!")
    