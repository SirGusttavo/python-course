# 3. Verificação de Nome de Cidade
# Crie um programa que leia o nome de uma cidade e verifique se ela começa ou não com o nome "Santo".

cidade = str(input('Digite o nome da cidade: '))
if "Santo" in cidade:
    print('Essa cidade possuí o nome "Santo" ')
else:
    print('Essa cidade não possuí o nome "Santo" ')
